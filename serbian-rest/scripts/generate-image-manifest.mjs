import fs from "node:fs";
import path from "node:path";

const projectRoot = process.cwd();
const publicDir = path.join(projectRoot, "public");

const IMAGE_EXTS = new Set([".jpg", ".jpeg", ".png", ".webp", ".avif", ".gif", ".jfif"]);

function normalizeDishName(input) {
  return String(input ?? "")
    .trim()
    .toLowerCase()
    .replaceAll("ё", "е")
    .replaceAll("«", "\"")
    .replaceAll("»", "\"")
    .replaceAll("—", "-")
    .replaceAll("–", "-")
    .replace(/[.,:;!?()[\]{}]/g, " ")
    .replace(/["'`]/g, " ")
    .replace(/\s+/g, " ")
    .trim();
}

function slugifyRussianName(input) {
  // Keep Cyrillic (works in URLs and filenames) but normalize separators.
  return normalizeDishName(input)
    .replace(/\s+/g, "-")
    .replace(/-+/g, "-")
    .replace(/^-|-$/g, "");
}

function walk(dir) {
  const out = [];
  if (!fs.existsSync(dir)) return out;
  const entries = fs.readdirSync(dir, { withFileTypes: true });
  for (const e of entries) {
    const full = path.join(dir, e.name);
    if (e.isDirectory() || e.isSymbolicLink()) {
      // On Windows, Junctions often appear as SymbolicLink Dirent.
      // Stat to confirm it’s a directory we can recurse into.
      try {
        const st = fs.statSync(full);
        if (st.isDirectory()) out.push(...walk(full));
        else out.push(full);
      } catch {
        // If target is not accessible, skip gracefully.
      }
    } else {
      out.push(full);
    }
  }
  return out;
}

function toPublicPath(absFile) {
  const rel = path.relative(publicDir, absFile).split(path.sep).join("/");
  return "/" + rel;
}

function buildIndex() {
  const files = walk(publicDir).filter((f) => IMAGE_EXTS.has(path.extname(f).toLowerCase()));

  const bySlug = {};
  const byNormalized = {};
  const all = [];

  for (const abs of files) {
    const base = path.basename(abs, path.extname(abs));
    const slug = slugifyRussianName(base);
    const norm = normalizeDishName(base);
    const publicPath = toPublicPath(abs);

    all.push(publicPath);
    if (slug && !bySlug[slug]) bySlug[slug] = publicPath;
    if (norm && !byNormalized[norm]) byNormalized[norm] = publicPath;
  }

  return { bySlug, byNormalized, all };
}

const manifest = buildIndex();
const outFile = path.join(projectRoot, "src", "data", "imageManifest.ts");

const content =
  `/* AUTO-GENERATED FILE. DO NOT EDIT.\n` +
  `   Generated by scripts/generate-image-manifest.mjs\n` +
  `   It indexes images under /public for dish name resolution.\n` +
  `*/\n\n` +
  `export const imageManifest = ${JSON.stringify(manifest, null, 2)} as const;\n`;

fs.mkdirSync(path.dirname(outFile), { recursive: true });
fs.writeFileSync(outFile, content, "utf8");

console.log(`[images:manifest] Indexed ${manifest.all.length} images into src/data/imageManifest.ts`);

